#declaring pi(3.14) value to a variable named PI
PI=3.14
#Taking radius as input from users
radius=float(input('please enter a value:'))
#Calculating circumference and area of a circle by using formulas and assigning it to a variable
_circum_of_circle_=2*radius
_area_of_circle_=PI*radius*radius
print("Circumference of a circle:",_circum_of_circle_)
print("Area of a circle",_area_of_circle_)
